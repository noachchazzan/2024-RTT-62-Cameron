package org.example.PA303_5_1;

public class timesTable {
    public static void main(String[] args) {
        for (int i = 1; i <= 12;i++) {
            for (int j = 1; j <= 12;j++) {
                System.out.print(j*i + " ");
            }
            System.out.println(" ");
        }
    }

}
